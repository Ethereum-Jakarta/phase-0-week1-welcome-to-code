// Looping Pertama

for (let k = 1; k <= 10; k++) {
    console.log(k);
}

console.log('Ini adalah Looping Pertama (Soal 2)')

// Looping Kedua

for (let l = 10; l >= 1; l--) {
    console.log(l);
}

console.log('Ini adalah Looping Kedua (Soal 2)');