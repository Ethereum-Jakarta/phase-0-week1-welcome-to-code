console.log(`Looping Pertama (maju)`);
for(let i = 1; i <= 10; i++){
    console.log(`Iterasi ke - ${i}`);
}
console.log();
console.log(`Looping Kedua (mundur)`);
for(let j = 10; j >= 1; j--){
    console.log(`Iterasi ke - ${j}`);
}
