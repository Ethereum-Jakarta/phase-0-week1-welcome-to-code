// PERULANGAN PERTAMA
perulangan_pertama = 1;
console.log("\n\nLOOPING PERTAMA\n");
while (perulangan_pertama <= 10) {
    console.log(perulangan_pertama);
    perulangan_pertama++;
}

// PERULANGAN KEDUA
