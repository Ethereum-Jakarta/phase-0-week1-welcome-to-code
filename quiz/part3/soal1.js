// Looping Pertama
console.log("LOOPING PERTAMA");
let i = 2;
while (i <= 20) {
  console.log(i + " - Belajar Looping");
  i += 2;
}

console.log("\n");

// Looping Kedua
console.log("LOOPING KEDUA");
let j = 20;
while (j >= 2) {
  console.log(j + " - Belajar Looping");
  j -= 2;
}
