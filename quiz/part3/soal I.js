//tampilkan output yang menunjukan kalau looping itu maju pada looping pertama dan mundur pada looping kedua
let a = 10;  {
    while (a <= 20) {
        console.log('FIRST LOOPING', a);
        a++;
    }
};

let x = 20; { 
    while (x >= 10) {
        console.log('SECOND LOOPING', x);
        x--;
    }
};
