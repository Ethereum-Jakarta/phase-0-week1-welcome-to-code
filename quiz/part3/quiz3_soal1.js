// Looping Pertama

let i = 1

while (i <= 10) {
    console.log(i);
    i++
}

console.log('Itu Looping Pertama (Soal 1)')

// Looping Kedua

let j = 10;

while (j >= 1) {
    console.log(j);
    j--
}

console.log('Itu Looping Kedua (Soal 1)')